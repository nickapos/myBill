    private void calcAmortizationTableButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcAmortizationTableButtonActionPerformed

        //make sure all the required fields have values in them
        String amountStr = amountTextField.getText().replaceAll(",", ".");
        String periodStr = periodTextField.getText();
        String rateStr = rateTextField.getText().replaceAll(",", ".");

        if (amountStr.length() > 0 && periodStr.length() > 0 && rateStr.length() > 0) {
            //calculate the amortization
            Amortization am = this.calculateAmortization();
            //initialize the amortization table object
            AmortizationTable amTable=new AmortizationTable(this.createTableHeader(),am);
            AmortizationTableWindow a = new AmortizationTableWindow();
            a.setAmTableString(amTable.amortizationTableString());
            a.runAmTable();

        }


    }//GEN-LAST:event_calcAmortizationTableButtonActionPerformed

    private Amortization calculateAmortization() {
        String amountStr = amountTextField.getText().replaceAll(",", ".");
        String periodStr = periodTextField.getText();
        String rateStr = rateTextField.getText().replaceAll(",", ".");
        double amount = Double.parseDouble(amountStr);
        double period = Double.parseDouble(periodStr);
        double rate = Double.parseDouble(rateStr);

        Amortization am = null;
        int amortizationPayoffFrequencySelection = payoffFrequencyComboBox.getSelectedIndex();
        if (amortizationPayoffFrequencySelection == 0)//if the user has selected monthly frequency
        {
            am = new Amortization(amount, period, rate, 12);
        } else if (amortizationPayoffFrequencySelection == 1)//if the user has selected 3 month periods
        {
            am = new Amortization(amount, period, rate, 4);
        } else if (amortizationPayoffFrequencySelection == 2)//if the user has selected 6 month periods
        {
            am = new Amortization(amount, period, rate, 2);
        } else if (amortizationPayoffFrequencySelection == 3)//if the user has selected 1 year periods
        {
            am = new Amortization(amount, period, rate, 1);
        }

        return am;
    }

    /**
     * This method will fill a string with the details from the amortization form
     * related with period, amount, interest rate
     *
     * @return
     */
    private String createTableHeader()
    {
        String th="";
        th+=java.util.ResourceBundle.getBundle("i18n/myBillUIBundle").getString("AMOUNT: ")+amountTextField.getText();
        th+="\n"+java.util.ResourceBundle.getBundle("i18n/myBillUIBundle").getString("DURATION: ")+periodTextField.getText();
        th+="\n"+java.util.ResourceBundle.getBundle("i18n/myBillUIBundle").getString("INTEREST RATE: ")+rateTextField.getText();
        th+="\n"+java.util.ResourceBundle.getBundle("i18n/myBillUIBundle").getString("FREQUENCY: ")+payoffFrequencyComboBox.getSelectedItem().toString();

        return th;
    }

